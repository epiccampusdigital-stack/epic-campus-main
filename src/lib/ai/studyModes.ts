export const STUDY_MODES_BY_ID = Object.fromEntries(
  STUDY_MODE_CONFIGS.map((m) => [m.id, m]),
) as Record<StudyMode, StudyModeConfig>

export function getModeConfig(mode: StudyMode): StudyModeConfig {
  return STUDY_MODE_CONFIGS.find((c) => c.id === mode) ?? STUDY_MODE_CONFIGS[0]
}

export function getDefaultModeForCourse(courseId?: string): StudyMode {
  if (!courseId) return 'general'
  if (courseId.includes('japan') || courseId.includes('ssw') || courseId.includes('jft')) return 'japanese-grammar'
  if (courseId.includes('korea') || courseId.includes('topik')) return 'korean-basics'
  if (courseId.includes('ielts')) return 'ielts-writing'
  return 'general'
}
